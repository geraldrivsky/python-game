from dataclasses import dataclass
from enum import Enum
from typing import List


class MonsterCategory(Enum):
    NECROPHAGE = "Трупоед"
    RELICT     = "Реликт"
    INSECTOID  = "Инсектоид"
    CURSED     = "Проклятый"


@dataclass(frozen=True)
class MonsterData:
    key: str
    display_name: str
    category: MonsterCategory
    oil_weakness: str           # ключ в OILS
    oil_multiplier: float       # множитель урона при правильном масле
    hp: int
    damage: int
    dodge_chance: float
    critical_chance: float
    critical_multiplier: float
    # Нарратив
    clues: List[str]            # 4 улики — раунды 1, 2, 3, 4 (4й = идентификация)
    identified_text: str        # что Геральт говорит после идентификации
    oil_tip: str                # подсказка о нужном масле (только после идентификации)
    actions: List[str]          # флейвор-тексты действий монстра (циклически)
    geralt_thoughts: List[str]  # внутренний монолог (раунды 1-3, потом post-id)
    post_id_thoughts: List[str] # мысли после идентификации


