from time import sleep 
from sys import stdout

def slow_print(text, delay=0.039):
    """Выводит текст посимвольно с задержкой"""
    for char in text:
        stdout.write(char)
        stdout.flush()
        sleep(delay)
    print()

def paragraph_pause():
    """Пауза между абзацами"""
    sleep(0.9)

